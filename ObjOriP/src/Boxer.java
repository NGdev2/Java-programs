public class Boxer {
    int height;
    float weight;
    int birth_year;
    int age;
    String name;
    String category;

    Boxer () {} //конструктор класса

    Boxer (int height, float weight, int birth_year, int age, String name, String category){
        this.height = height;
        this.weight = weight;
        this.birth_year = birth_year;
        this.age = age;
        this.name = name;
        this.category = category;

    }


}
